// Funzione di aggiornamento lingua
export function updateLanguage(lang: 'it' | 'en') {
  // Aggiorna lo stato nel localStorage per persistenza al ricaricamento
  localStorage.setItem('app-lang', lang);
  
  // Seleziona tutti gli elementi con l'attributo data-i18n
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (key && translations[lang][key as keyof typeof translations['it']]) {
      // Sostituisce il contenuto di testo
      el.textContent = translations[lang][key as keyof typeof translations['it']];
    }
  });
}

// Funzione helper per ottenere un testo (utile per React state o attributi)
export function t(key: string, lang: 'it' | 'en'): string {
  return translations[lang][key as keyof typeof translations['it']] || key;
}

export default translations;
